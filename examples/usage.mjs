import absolutelyNothing, {
  doNothing,
  makeNothing,
  createVoid,
  noop,
  empty,
  getNothing,
  stillNothing,
  awaitNothing,
  fixYourCode,
  improvePerformance,
  solveAllProblems,
  enterpriseNothing,
  productionReadyNothing,
  scaleNothing,
} from 'absolutely-nothing';

// --- Basic usage ---

absolutelyNothing();   // Nothing happened.
doNothing();           // Still nothing.
makeNothing();         // Made it.
createVoid();          // Created.
noop();                // Classic.

// --- Retrieval ---

const result = getNothing();       // undefined
const more   = stillNothing();     // also undefined, but with commitment

// --- Async / modern applications ---

await awaitNothing();              // Resolved. To nothing.

// --- Problem solving ---

fixYourCode();         // Done. (It wasn't this library's fault anyway.)
improvePerformance();  // Benchmarks unchanged. You're welcome.
solveAllProblems();    // All of them. Every single one.

// --- Enterprise tier ---

enterpriseNothing();        // Comes with nothing SLA.
productionReadyNothing();   // Zero incidents since launch.
scaleNothing();             // Scaled to 0 requests per second.

// --- Default export ---

const nothing = absolutelyNothing;
nothing();  // Same result as always.
